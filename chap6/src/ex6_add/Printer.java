package ex6_add;

public class Printer {
    public static void println(int val){
        System.out.println(val);
    }
    public static void println(boolean val){
        System.out.println(val);
    }
    public static void println(double val){
        System.out.println(val);
    }
    public static void println(String val){
        System.out.println(val);
    }
}
